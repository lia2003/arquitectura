# Definir una lista de números
numeros = [1, 2, 3, 4, 5]

# Calcular la suma de los números en la lista
suma = sum(numeros)

# Calcular la cantidad de números en la lista
cantidad = len(numeros)

# Calcular la media aritmética
media = suma / cantidad

# Imprimir la media
print("La media aritmética es:", media)